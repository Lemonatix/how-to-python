# Syntax for creating and assigning a variable
variable_name = 1 # can be any value

message = "Hello World" # string 
quantity = 20 # integer
pi = 3.14 # float

# Reassigning Variables
x = "Hello world"
x = "Good morning" # new string value is "good morning"
x = True
print(f"x is {x}")

# Variables as expressions
a = 10
b = 20
c = a + b
print(f"c is {c}")