# integer int()
x = 14

# float float()
x = 3.14

# complex complex()
x = 3+4j

# string str()
x = "Hello World"

# list list()
x = ["apple", "banana", "cherry"]

# tuple tuple()
x = ("apple", "banana", "cherry")

# range range()
x = range(20)

# dictionary dict()
x = {"name" : "apple", "quantity" : 20}

# set set()
x = {"apple", "banana", "cherry"}

# frozenset frozenset()
x = frozenset({"apple", "banana", "cherry"})

# bool bool()
x = True

# bytes bytes()
x = b"apple"

# bytearray bytearray()
x = bytearray(5)

# memoryview memoryview()
x = memoryview(bytes(5))

# Nonetype -
x = None

print(x) # prints the last defined x